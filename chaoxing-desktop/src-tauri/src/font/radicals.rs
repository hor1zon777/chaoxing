#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_basic_radicals() {
        assert_eq!(replace_radicals("\u{2F00}"), "\u{4E00}"); // ⼀ → 一
        assert_eq!(replace_radicals("\u{2F08}"), "\u{4EBA}"); // ⼈ → 人
        assert_eq!(replace_radicals("\u{2FD5}"), "\u{9FA0}"); // ⿕ → 龠
    }

    #[test]
    fn test_simplified_radicals() {
        assert_eq!(replace_radicals("\u{2EA0}"), "\u{6C11}"); // ⺠ → 民
        assert_eq!(replace_radicals("\u{2EE2}"), "\u{9A6C}"); // ⻢ → 马
        assert_eq!(replace_radicals("\u{2EC5}"), "\u{89C1}"); // ⻅ → 见
        assert_eq!(replace_radicals("\u{2ED3}"), "\u{957F}"); // ⻓ → 长
    }

    #[test]
    fn test_gao_variant() {
        assert_eq!(replace_radicals("\u{9AD9}"), "\u{9AD8}"); // 髙 → 高
    }

    #[test]
    fn test_mixed_text() {
        // 普通汉字不变
        assert_eq!(replace_radicals("你好世界"), "你好世界");
        // 混合文本：康熙部首被替换，普通字符保留
        assert_eq!(replace_radicals("你\u{2F00}好"), "你\u{4E00}好");
    }

    #[test]
    fn test_empty_string() {
        assert_eq!(replace_radicals(""), "");
    }

    #[test]
    fn test_all_kangxi_count() {
        // 确保 U+2F00..=U+2FD5 范围内所有字符都被映射（不返回原字符）
        for code in 0x2F00..=0x2FD5_u32 {
            let ch = char::from_u32(code).unwrap();
            let replaced = replace_radicals(&ch.to_string());
            let replaced_ch = replaced.chars().next().unwrap();
            assert_ne!(
                ch, replaced_ch,
                "康熙部首 U+{:04X} 未被映射",
                code
            );
        }
    }
}
